package com.log.monitor.model;

public class LogModel {

	private String id;
	private String state;
	private String type;
	private String host;
	private Long timestamp;
	public String getId() {
		return id;
	}
	
	public String getState() {
		return state;
	}
	
	public String getType() {
		return type;
	}
	
	public String getHost() {
		return host;
	}
	
		
	public Long getTimestamp() {
		return timestamp;
	}
	
}
